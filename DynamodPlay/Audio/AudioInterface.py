from abc import ABC, abstractmethod

########################################################################
class AudioInterface(ABC):

	#----------------------------------------------------------------------
	@abstractmethod
	def getKey():
		pass

	#----------------------------------------------------------------------
	@abstractmethod
	def getStatus():
		pass

	#----------------------------------------------------------------------
	@abstractmethod
	def isPlaying():
		pass

	#----------------------------------------------------------------------
	@abstractmethod
	def play(play):
		pass

	#----------------------------------------------------------------------
	@abstractmethod
	def pause():
		pass

	#----------------------------------------------------------------------
	@abstractmethod
	def stop():
		pass
